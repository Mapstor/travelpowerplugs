// Add continent to each country
Object.entries(data).forEach(([iso2, country]) => {
  if (continentMap[iso2]) {
    country.continent = continentMap[iso2];
  } else {
    // Default to Africa for any unmapped countries
    console.log(`Warning: No continent mapping for ${iso2} (${country.name})`);
    country.continent = 'africa';
  }
});

// Save the updated data
fs.writeFileSync('./src/data/countries-plugs.json', JSON.stringify(data, null, 2));

// Count countries per continent
const continentCounts = {};
Object.values(data).forEach(country => {
  continentCounts[country.continent] = (continentCounts[country.continent] || 0) + 1;
});

